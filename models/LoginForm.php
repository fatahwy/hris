<?php

namespace app\models;

use app\helpers\DBHelper;
use Yii;
use yii\base\Model;

/**
 * LoginForm is the model behind the login form.
 *
 * @property-read User|null $user
 *
 */
class LoginForm extends Model
{
    public $email;
    public $password;
    public $rememberMe = true;

    private $_user = false;


    /**
     * @return array the validation rules.
     */
    public function rules()
    {
        return [
            // email and password are both required
            [['email', 'password'], 'required'],
            // rememberMe must be a boolean value
            ['rememberMe', 'boolean'],
            // password is validated by validatePassword()
            ['password', 'validatePassword'],
        ];
    }

    /**
     * Validates the password.
     * This method serves as the inline validation for password.
     *
     * @param string $attribute the attribute currently being validated
     * @param array $params the additional name-value pairs given in the rule
     */
    public function validatePassword($attribute, $params)
    {
        if (!$this->hasErrors()) {
            $user = $this->getUser();

            if (!$user) {
                $this->addError($attribute, 'Email tidak ditemukan.');
                return;
            } elseif (!$user->validatePassword($this->password)) {
                $this->addError($attribute, 'Password salah.');
                return;
            }

            $client = $user->client ?? null;
            // check company activity
            if ($client) {
                if (!$client->is_active) {
                    $this->addError($attribute, 'Akun perusahaan Anda belum aktif. Silakan cek email untuk aktivasi.');
                } else if ($client->expired_at < DBHelper::now()) {
                    $this->addError($attribute, 'Akun perusahaan Anda telah berakhir. Silakan hubungi admin untuk perpanjangan.');
                }
            }
        }
    }

    /**
     * Logs in a user using the provided email and password.
     * @return bool whether the user is logged in successfully
     */
    public function login()
    {
        if ($this->validate()) {
            return Yii::$app->user->login($this->getUser(), $this->rememberMe ? 3600 * 24 * 30 : 0);
        }
        return false;
    }

    /**
     * Finds user by [[email]]
     *
     * @return User|null
     */
    public function getUser()
    {
        if ($this->_user === false) {
            $this->_user = User::findByEmail($this->email);
        }

        return $this->_user;
    }
}